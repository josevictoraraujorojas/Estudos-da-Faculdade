package Java.Lista03;

public class Ex16 {
//    Questão 16 Escreva o comando de um laco de repetição fore outro do while para imprimir ?oi?15 vezes.

    public static void main(String[] args) {
        int num=1;
        do {
            ++num;
            System.out.println("oi");
        }while (num<=15);
    }
}
